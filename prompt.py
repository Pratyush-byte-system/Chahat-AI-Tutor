def get_full_prompt() -> str:
    """
    Build the complete system prompt by combining:
    1. Base CHAHAT_PROMPT (personality, teaching style, tools)
    2. Dynamic curriculum roadmap (based on active goals)
    3. Memory context (past sessions, topics covered, where to continue)
    
    This is called at session start so CHAHAT remembers everything.
    """
    from memory import build_memory_context, build_curriculum_prompt

    # Inject dynamic curriculum roadmap
    curriculum_section = build_curriculum_prompt()
    prompt = CHAHAT_PROMPT.replace("{{DYNAMIC_CURRICULUM_ROADMAP}}", curriculum_section)

    memory_context = build_memory_context()
    if memory_context:
        return prompt + "\n" + memory_context
    return prompt